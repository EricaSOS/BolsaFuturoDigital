/********************** CRIACAO DAS TABELAS ************************/
CREATE TABLE CLIENTE (
  COD_CLI      INTEGER   NOT NULL,
  NOME_CLI     TEXT(40)  NOT NULL,
  ENDERECO     TEXT(40),
  CIDADE       TEXT(20),
  CEP          TEXT(8),
  UF           TEXT(2),
  PRIMARY KEY (COD_CLI)
);

CREATE TABLE VENDEDOR (
  COD_VEND     INTEGER   NOT NULL,
  NOME_VEND    TEXT(40)  NOT NULL,
  SAL_FIXO     REAL(9,2) NOT NULL,
  FAIXA_COMISS TEXT(1)   NOT NULL,
  PRIMARY KEY (COD_VEND)
);

CREATE TABLE PRODUTO (
  COD_PROD     INTEGER   NOT NULL,
  UNID_PROD    TEXT(3)   NOT NULL,
  DESC_PROD    TEXT(20)  NOT NULL,
  VAL_UNIT     REAL(9,2) NOT NULL,
  PRIMARY KEY (COD_PROD)
);

CREATE TABLE PEDIDO (
  NUM_PED      INTEGER   NOT NULL,
  PRAZO_ENTR   INTEGER   NOT NULL,
  CD_CLI       INTEGER   NOT NULL,
  CD_VEND      INTEGER   NOT NULL,
  PRIMARY KEY (NUM_PED),
  FOREIGN KEY (CD_CLI)  REFERENCES CLIENTE (COD_CLI),
  FOREIGN KEY (CD_VEND) REFERENCES VENDEDOR (COD_VEND)
);

CREATE TABLE ITEM_PEDIDO (
  NO_PED       INTEGER   NOT NULL,
  CD_PROD      INTEGER   NOT NULL,
  QTD_PED      REAL      NOT NULL,
  FOREIGN KEY (NO_PED)  REFERENCES PEDIDO (NUM_PED),
  FOREIGN KEY (CD_PROD) REFERENCES PRODUTO (COD_PROD)
);

